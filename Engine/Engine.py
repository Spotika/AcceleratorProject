class Engine:
    """
    Класс основного движка программы
    """

    @classmethod
    def start(cls) -> None:
        """
        Запуск и настройка всех компонентов
        """

    @classmethod
    def update(cls) -> None:
        """
        Обновляет все компоненты
        """

    @classmethod
    def start_game(cls, game_name: str) -> None:
        """
        Запускает игру
        :param game_name: имя игры
        """